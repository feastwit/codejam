  function getRandomIndex(arr) {
    let len = arr.length;
      for(let i = len-1; i>=0; i--) {
        let randomIndex = Math.floor(Math.random()*(i+1));
        let itemIndex = arr[randomIndex];
        arr[randomIndex]=arr[i];
        arr[i] = itemIndex;
  }
    return arr 
  }

